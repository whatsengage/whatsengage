// Default Language: HINDI
let currentLang = "hi";

// Cache elements for performance
const i18nEls = Array.from(document.querySelectorAll("[data-i18n]"));
const placeholderEls = Array.from(document.querySelectorAll("[data-placeholder]"));

// Function to apply the current language to the DOM
function applyLanguage() {
  // --- UPDATED LOGIC: Show the TARGET language ---
  const langLabel = document.getElementById("lang-label");
  if(langLabel) {
      // If current is EN, button says "рд╣рд┐рдВрджреА" (Switch to Hindi)
      // If current is HI, button says "English" (Switch to English)
      langLabel.innerText = currentLang === "en" ? "рд╣рд┐рдВрджреА" : "English";
  }
  
  const mobileLangLabel = document.getElementById("mobile-lang-label");
  if(mobileLangLabel) {
      mobileLangLabel.innerText = currentLang === "en" ? "Switch to Hindi (рд╣рд┐рдВрджреА)" : "Switch to English";
  }

  // Update Text Elements
  i18nEls.forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const value = translations[currentLang][key];
    if (value !== undefined) {
      if (/<[a-z][\s\S]*>/i.test(value)) {
        element.innerHTML = value;
      } else {
        element.textContent = value;
      }
    }
  });

  // Update Inputs Placeholders
  placeholderEls.forEach((element) => {
    const key = element.getAttribute("data-placeholder");
    const ph = translations[currentLang][key];
    if (ph !== undefined) {
      element.setAttribute("placeholder", ph);
    }
  });

  // Update Plan Feedback Label if visible
  const feedbackLabel = document.getElementById("feedback-label");
  if (feedbackLabel && !feedbackLabel.parentElement.classList.contains("hidden")) {
     feedbackLabel.textContent = translations[currentLang]["feedback_label"];
  }
}

// Function called when button is clicked
function toggleLanguage() {
  currentLang = currentLang === "en" ? "hi" : "en";
  applyLanguage();

  // Add fade animation only on toggle
  i18nEls.forEach(el => {
    el.classList.remove("fade-in");
    void el.offsetWidth; // trigger reflow
    el.classList.add("fade-in");
  });
}

// Plan Selection Logic
function selectPlan(planName) {
  const planInput = document.getElementById("plan_input");
  if (planInput) planInput.value = planName;

  const feedbackBox = document.getElementById("plan-feedback");
  const feedbackName = document.getElementById("plan-name-display");
  const feedbackLabel = document.getElementById("feedback-label");

  if (feedbackBox && feedbackName) {
    feedbackBox.classList.remove("hidden");
    if (feedbackLabel) {
       feedbackLabel.textContent = translations[currentLang]["feedback_label"];
    }

    let displayPlan = planName;
    if (currentLang === "hi") {
      if (planName === "Trial") displayPlan = "рдЯреНрд░рд╛рдпрд▓";
      if (planName === "Starter") displayPlan = "рд╕реНрдЯрд╛рд░реНрдЯрд░";
      if (planName === "Pro") displayPlan = "рдкреНрд░реЛ";
    }
    feedbackName.textContent = displayPlan;
  }

  setTimeout(() => {
    const nameInput = document.querySelector('[name="name"]');
    if (nameInput) nameInput.focus();
  }, 600);
}

